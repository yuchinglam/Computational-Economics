# ╔═╡ 7d4f06de-f312-4a10-aa14-673491a628a5
md"

The second approach is the main focus in this section. Now, we do not take $\mu^*$ as  given. Instead, we solve the optimization problem to simultaneously obtain the optimal investment $\mu^*$, the optimal contract $(W_1,W_2)$ and the corresponding expected utility $E(U)$.

According to the model, the optimal contract $(W_1,W_2)$ maximizes the expected utility stated in equation (23), subject to (i) the zero profit constraint in equation (20), and (ii) the worker's investment behavior in equation (21). Here we first construct a maximization problem with three unknowns $(W_1,W_2,\mu^*)$ and two non-linear equality constraints.
"

# ╔═╡ 5f9f748f-9339-4659-9ced-c5087d0d3001
# define expected utility maximization problem under contest

function maxEU✶(σ²::Float64, y₀::Int64)
	
	m = Model(NLopt.Optimizer)
	set_optimizer_attribute(m, "algorithm", :LD_MMA)

	# W₁ = x, W₂ = y, μ✶ = z
	@variable(m, x)
	@variable(m, y)
	@variable(m, z >= 0)
	
	# maximize expected utility (23)
	@NLobjective(m, Max, 0.5 * 0.5 *(y₀ + x-z^2/2)^0.5+0.5 * 0.5*(y₀ +y-z^2/2)^0.5)

	# equality constraint (20)
	@NLconstraint(m, z >= 0.5*(x+y))
	@NLconstraint(m, z <= 0.5*(x+y))

	# equality constraint (21)
	@NLconstraint(m, sqrt(σ²)*sqrt(pi)*z*(0.25*(y₀ + x-z^2/2)^(-0.5)+0.25*(y₀ + y-z^2/2)^(-0.5)) >= 0.5*(y₀ + x-z^2/2)^0.5-0.5*(y₀ + y-z^2/2)^0.5) 
	@NLconstraint(m, sqrt(σ²)*sqrt(pi)*z*(0.25*(y₀ + x-z^2/2)^(-0.5)+0.25*(y₀ + y-z^2/2)^(-0.5)) <= 0.5*(y₀ + x-z^2/2)^0.5-0.5*(y₀ + y-z^2/2)^0.5)
	
	set_start_value(x, 0)
	set_start_value(y, 0)
	set_start_value(z, 0)
	
	JuMP.optimize!(m)

	# return (maximized expected utility, optimal investment μ✶)
	return  (objective_value(m), value(z)) 
	
end

