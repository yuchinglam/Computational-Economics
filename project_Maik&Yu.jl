md"
## Section 1 Optimal Linear Piece Rate

Here we will replicate columns 2 and 4 in table 1. $\mu$ is the worker's optimal investment and $E(U)$ is the expected utility under piece rate. The main difference between the two approaches is to fix $\mu$ first or not. We will discuss in detail in the following subsections.

### Section 1.1 First approach

The first approach is to fix the optimal values of investment $\mu$, then compute the corresponding expected utility.

For the optimal investment μ, we follow the Taylor series approximation given in equation (16). The paper states that the measure of absolute risk aversion, s, is evaluated at mean income. Consider that the non-labor income contributes a large proportion to the total income (by the parameter setting), we use the non-labor income y₀ as a proxy of the mean income. It follows that the expression of optimal investment $\mu$ can be simplified as follows.

"

# ╔═╡ 2e856a9a-c683-40d8-b8d7-08942af9797b
μ(y₀,σ²) = V / (1 + 0.5/y₀ * σ²) # equation (16)

# ╔═╡ 46df1012-dc33-4f24-81d0-906c08de6655
md"
After obtaining the values of $\mu$, we then proceed to find the expected utility, take $\mu$ as given. We first generate a large number of draws of net income. Then, by taking the average of the corresponding utility levels, we obtain an approximation of expected utility $E(U)$.
"

# ╔═╡ 2e683867-736c-46c4-ba13-4dd496e4b0f4
n = 100000

# ╔═╡ d6843d0c-71ea-4a74-bff5-23fcc5cd95b5
# generate draws of income under different (1)y₀, (2)σ², and (3)scheme 
# ** take μ as given

function makeData_1(n::Int64, y₀::Int64, σ²::Float64)

	y = zeros(n)
	dist = Normal(0, sqrt(σ²))
	
	for i in 1:n		
		ε = rand(dist)
		y[i] = y₀ + V * μ(y₀,σ²) + μ(y₀,σ²) * ε - μ(y₀,σ²)^2 / 2	
	end

	return y

end

# ╔═╡ 0f95483f-0f90-4564-a512-187a51b698ef
# Table 1, column 4
# take average over utility of sample income

begin
	# expected utility under piece rate, y₀ = 100
	EU₁_1 = zeros(6)
	for i in 1:6
		EU₁_1[i] = mean(U, makeData_1(n, 100, σ₁²[i]))
	end

	# expected utility under piece rate, y₀ = 25
	EU₂_1 = zeros(4)
	for i in 1:4
		EU₂_1[i] = mean(U, makeData_1(n, 25, σ₂²[i]))
	end
end

