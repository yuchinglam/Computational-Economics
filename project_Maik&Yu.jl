## Section 2: Optimum Prize Structure


Now, we turn to replicate column 3 and 5. $\mu^*$ is the worker's optimal investment and $E(U^*)$ is the expected utility under contest.

Again, there are two approaches to obtain the value of $\mu^*$. The first approach is an analogy to the one discussed in section 1.1. We follow the Taylor series approximation given in equation (24). It follows that the expression of optimal investment $\mu$ can be simplified as follows and easily computed.

