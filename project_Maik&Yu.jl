# Rank-Order Tournaments as Optimum Labor Contracts
The paper studies compensation schemes which pay according to an individual's ordinal rank in an organization rather than his output level. They compare the rank-tournament and the linear piece rate schemes. In both schemes, the worker chooses the investment $\mu$, which determines the mean of the stochastic output level $q$, with convex investment cost $C\left(\mu\right)>0$. The utility of the worker is $U\left(y\right)$, where $y$ is the total income net of investment cost. The value of output is $V$. We will briefly state the key equations of the model as follows. For simplicity, we follow the exact numbering of equations in the paper.



"



# ╔═╡ 6dcbca44-6488-4482-ac15-147ae46c2730
md"
## Optimal Linear Piece Rate
Under linear piece rate $r$, the worker earns labor income $rq$ when his output level is $q$. 


The optimal contract maximizes

$$\begin{align}
\int U\left\{V \mu\left(r\right)+r \varepsilon - C\left[\mu\left(r\right)\right]\right\}f\left(\varepsilon\right) d\varepsilon
\end{align}$$

with respect to $r$, where $\mu=\mu\left(r\right)$ satisfies the worker's investment behavior **(13)**

$$\begin{align*}
r=C'\left(\mu\right).
\end{align*}$$


By Taylor approximation, the optimum is approximated by equation **(16)**

$$\begin{align*}
\mu \approx C'^{-1}\left(\frac{V}{1+sC'' \sigma^2}\right)
\end{align*}$$

where $s\equiv -\frac{U''}{U'}$ is evaluated at mean income.

"

# ╔═╡ 30f41f38-a9c5-4f6b-a97f-4cba36471a4d
md"

## Optimum Prize Structure

Under two-player rank-order tournament, the worker with a high output earns a fixed prize $W_1$ and the one with a lower output earns a fixed prize $W_2$.

The optimal contract $\left(W_1,W_2\right)$ maximizes **(23)**

$$\begin{align*}
\frac{1}{2} U\left[W_1 - C\left(\mu^*\right)\right]+\frac{1}{2} U\left[W_2 - C\left(\mu^*\right)\right],
\end{align*}$$

subject to the zero-profit constraint **(20)**

$$\begin{align*}
V\mu^*=\frac{1}{2} W_1+\frac{1}{2} W_2
\end{align*}$$

and the worker's investment behavior **(21)**

$$\begin{align*}
C\left(\mu^*\right)=\frac{2\left\{U\left[W_1-C\left(\mu^*\right)\right]-U\left[W_2-C\left(\mu^*\right)\right]\right\}g\left(0\right)}{U'\left[W_1-C\left(\mu^*\right)\right]-U'\left[W_2-C\left(\mu^*\right)\right]}.
\end{align*}$$

By Taylor approximation, the optimum is approximated by equation **(24)**

$$\begin{align*}
\mu^* \approx C'^{-1}\left(\frac{V}{1+sC'' \sigma^2\pi}\right).
\end{align*}$$

where $s\equiv -\frac{U''}{U'}$ is evaluated at mean income.

"

