### Section 1.2: Second approach

We analytically derive the worker’s ex-post utility level under the optimal ex-ante investment. We draw a random vector of length 1 Mio from a normal distribution. For different piece rates (close to the optimal one which we know from the paper), we plug the shocks into the ex-post utility function to generate a table with piece rate values as rows and shocks as columns (the entries in the table are ex-post utility levels). For a given piece rate, we find the expected utility by calculating the mean for each row. The piece rate that yields the highest expected utility is the optimal piece rate.
"

# ╔═╡ 0615d43a-6486-47cd-9828-cdd9b16fe61f
r = LinRange(0.8,1,2001)

# ╔═╡ 57bfae79-fdc0-4d5b-b816-896717f4edfd
# generate draws of ex post utility under different (1)y₀, (2)σ², and (3)scheme
# ** r is not fixed

function makeData_2(n::Int64, y₀::Int64, σ²::Float64)

	utility = zeros(size(r,1),n)
	dist = Normal(0, sqrt(σ²))
	
	for i in 1:size(r,1)		
		for j in 1:n		
			ε = rand(dist)
			utility[i,j] = U(y₀ + r[i] + r[i] * ε - r[i]^2 / 2)	
		end
	end
	
	return utility

end

# ╔═╡ da20fdc9-d8cd-4a73-a0d2-9e074dff478f
begin	
	# expected utility under piece rate, y₀ = 100
	EU₁_2 = zeros(6)
	μ₁_2 = zeros(6)
	expectedU₁ = zeros(size(r,1))
	for i in 1:6
		expectedU₁ = mean(makeData_2(n,100,σ₁²[i]), dims = 2)
		EU₁_2[i] = maximum(expectedU₁)
		μ₁_2[i] = r[argmax(expectedU₁)[1]]
	end

	# expected utility under piece rate, y₀ = 25
	EU₂_2 = zeros(4)
	μ₂_2 = zeros(4)
	expectedU₂ = zeros(size(r,1))
	for i in 1:4
		expectedU₂ = mean(makeData_2(n,25,σ₂²[i]), dims = 2)
		EU₂_2[i] = maximum(expectedU₂)
		μ₂_2[i] = r[argmax(expectedU₂)[1]]
	end
end

